/*
A variable is the name of a reserved area allocated in memory. 
In other words, it is a name of the memory location. It is a combination of "vary + able" 
which means its value can be changed */

// Lets understand the concept and type of variables in java.

class variables
{
    static int a = 10;      // Static Variable

    void m1()
    {
        int b = 20;     // Local Variable
    }

    public static void main(String[] args) {
        int c = 30;     // Instance variable
    }
}
